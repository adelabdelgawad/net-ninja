/// Escape HTML special characters
fn escape_html(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
        .replace('\'', "&#39;")
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_escape_html() {
        assert_eq!(escape_html("<script>alert('xss')</script>"),
                   "&lt;script&gt;alert(&#39;xss&#39;)&lt;/script&gt;");
    }

    #[test]
    fn test_build_email_no_results() {
        let html = build_task_notification_email(
            "Test Task",
            1,
            Utc::now(),
            None,
            None,
        );
        assert!(html.contains("Execution Time:"));
        assert!(!html.contains("Task ID:"));
        assert!(!html.contains("Combined Results"));
    }
}
